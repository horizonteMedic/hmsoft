     public void Actualizar() {
        String sCodigo = txtNorden.getText();
        String strSqlStmt;
        strSqlStmt = "UPDATE  panel10d\n "
                + "   SET fecha_examen='" + FechaEx.getDate() + "',"
                + "       txtmetodo='" + txtMetodo.getText() + "',"
                + "       txtcocaina='" + txtCocaina.getText() + "',"
                + "       txtmarihuana='" + txtMarihuana.getText() + "',"
                + "       txtanfetamina='" + txtAnfetamina.getText() + "',"
                + "       txtmetanfetamina='" + txtMetanfetamina.getText() + "',"
                + "       txtbenzodiacepina='" + txtBenzodiacepina.getText() + "',"
                + "       txtopiaceos='" + txtOpiaceos.getText() + "',"
                + "       txtbarbituricos='" + txtBarbituricos.getText() + "',"
                + "       txtmetadona='" + txtMetadona.getText() + "',"                
                + "       txtfenciclidina='" + txtFenciclidina.getText() + "',"
                + "       txtantidepresivos='" + txtAntidepresivos.getText() + "'"
                + "   WHERE n_orden=" + sCodigo + ";";
       
        //  System.out.println(strSqlStmt);
        //oFunc.SubSistemaMensajeInformacion(strSqlStmt);
        if (oConn.FnBoolQueryExecuteUpdate(strSqlStmt)){
            oFunc.SubSistemaMensajeInformacion("Se ha actualizado la Entrada con Éxito");
            imprimir();
            limpiar();
            try {
                oConn.sqlStmt.close();
            } catch (SQLException ex) {
                Logger.getLogger(InformePsicolaboral.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            oFunc.SubSistemaMensajeError("No se pudo Agregar La Entrada");
        }
    }
     
