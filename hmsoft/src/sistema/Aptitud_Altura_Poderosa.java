    private void txtNordenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNordenActionPerformed
          FechaNacimiento = new com.toedter.calendar.JDateChooser();
       if(!txtNorden.getText().isEmpty()){
        
          if(!OrdenExiste()){     
       String Sql="SELECT d.cod_pa, d.nombres_pa||' '||d.apellidos_pa as nombre, d.fecha_nacimiento_pa ,d.sexo_pa,"
               + "d.ocupacion_pa,n.razon_empresa,n.razon_contrata ,n.n_orden, n.cargo_de, a.txtobservacionesfm,"
               + "n.nom_examen,l.txthemoglobina, l.txthematocrito, l.txtvsg, \n" +
"                l.txtglucosabio, l.txtcreatininabio  \n" +
"                FROM datos_paciente AS d \n" +
"                INNER JOIN n_orden_ocupacional AS n ON (d.cod_pa = n.cod_pa) \n" +
"               left JOIN anexo7c AS a ON (a.n_orden = n.n_orden) \n" +
"	       left JOIN lab_clinico AS l ON (l.n_orden = n.n_orden)  "
               + "WHERE n.n_orden ='"+txtNorden.getText().toString() +"'";
         oConn.FnBoolQueryExecute(Sql);
                try {
                    if (oConn.setResult.next()) {
                        txtNombres.setText(oConn.setResult.getString("nombre"));
                        txtEmpresa.setText(oConn.setResult.getString("razon_empresa")); 
                        txtContratista.setText(oConn.setResult.getString("razon_contrata"));
                        txtDni.setText(oConn.setResult.getString("cod_pa"));
                        txtTipoExamen.setText(oConn.setResult.getString("nom_examen"));
                        txtPuestoPostula.setText(oConn.setResult.getString("cargo_de"));
                        
                        String sexo=oConn.setResult.getString("sexo_pa");
                       txtGenero.setText(sexo.equals("M")?"MASCULINO" : "FEMENINO");    
                        FechaNacimiento.setDate(oConn.setResult.getDate("fecha_nacimiento_pa"));
                        txtEdad.setText(String.valueOf(oFunc.calcularEdad(FechaNacimiento.getCalendar())) );
                        txtNorden.setEditable(false);
                     // FechaHoy.requestFocusInWindow();
                        txtHemoglobina.setText(oConn.setResult.getString("txthemoglobina"));
                        txtVSG.setText(oConn.setResult.getString("txtvsg"));
                        txtGlucosaBio.setText(oConn.setResult.getString("txtglucosabio"));
                        txtCreatininaBio.setText(oConn.setResult.getString("txtcreatininabio"));
                        if(!txtHemoglobina.getText().isEmpty() && !"N/A".equals(txtHemoglobina.getText().toString())){
                        float hemoglobina=Float.parseFloat(txtHemoglobina.getText().toString());
                              if ("M".equals(sexo)) {
                                  if(hemoglobina < 14 || hemoglobina > 20){
                                       txtHemoglobina.setForeground(Color.red);
                                   }
                                  else{
                                      txtHemoglobina.setForeground(Color.BLACK);
                                  }
                              }
                              if ("F".equals(sexo)) {
                                   if(hemoglobina < 13.5 || hemoglobina > 20){
                                        txtHemoglobina.setForeground(Color.red);
                                   }
                                   else{
                                      txtHemoglobina.setForeground(Color.BLACK);
                                  }
                              }
                        }
                        if (!txtGlucosaBio.getText().isEmpty() && !"N/A".equals(txtGlucosaBio.getText().toString())) {
                            float glucosa = Float.parseFloat(txtGlucosaBio.getText().toString());
                            if (glucosa >= 110 || glucosa < 70) {
                                txtGlucosaBio.setForeground(Color.red);
                            } else {
                                txtGlucosaBio.setForeground(Color.black);
                            }
                        }
                        if(!txtCreatininaBio.getText().isEmpty()&&!"N/A".equals(txtCreatininaBio.getText().toString())){
                        float cretinina=Float.parseFloat(txtCreatininaBio.getText().toString());
                        if(cretinina>=1.4 || cretinina<0.8){
                            txtCreatininaBio.setForeground(Color.red);
                        }else{
                            txtCreatininaBio.setForeground(Color.black);
                        }
                        }
                        Fecha();
                        fechaHasta();
                        muestraVisual();
                       }else{
                        oFunc.SubSistemaMensajeError("No se encuentra Algunos Registros necesarios(Alta en Ex-Ocupacionales)");
                    }
                oConn.setResult.close();
                oConn.sqlStmt.close();
            } catch (SQLException ex) {
            oFunc.SubSistemaMensajeInformacion("Odontograma:" + ex.getMessage().toString());}
          }else { oFunc.SubSistemaMensajeInformacion("NÂ° Orden registrado" );}
           } 
    }//GEN-LAST:event_txtNordenActionPerformed
