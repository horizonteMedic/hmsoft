private void sbCargarDatos(String valor, JTable tabla){
        
          String [] titulos={"N° Orden","Nombres","Fecha"};
          String [] registros = new String[3];
          model = new DefaultTableModel(null,titulos){        
              @Override
          public boolean isCellEditable(int rowIndex, int columnIndex) {
        return false;
    }};
          String Sql="";
          if(!txtBuscarCod.getText().isEmpty()){
              Sql="select d.nombres_pa||''||d.apellidos_pa AS nombres , p.fecha_perimetro,p.n_orden "
                  + "FROM datos_paciente AS d "
                  + "INNER JOIN n_orden_ocupacional AS n ON (d.cod_pa = n.cod_pa)"
                  + "INNER JOIN perimetro_toracico AS p ON (n.n_orden = p.n_orden)"
                      + "where p.n_orden='"+txtBuscarCod.getText().toString()+"'"; 
          }else{
              Sql="select d.nombres_pa||''||d.apellidos_pa AS nombres , p.fecha_perimetro,p.n_orden "
                  + "FROM datos_paciente AS d "
                  + "INNER JOIN n_orden_ocupacional AS n ON (d.cod_pa = n.cod_pa)"
                  + "INNER JOIN perimetro_toracico AS p ON (n.n_orden = p.n_orden)where CONCAT(nombres_pa,' ',apellidos_pa) LIKE '%"+valor+"%'"; 
          }
         SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
    if (oConn.FnBoolQueryExecute(Sql))
        {
             try  {
                
                while (oConn.setResult.next())
                {
                                     
                    registros[0]= oConn.setResult.getString("n_orden");
                    registros[1]= oConn.setResult.getString("nombres");
                    registros[2]= formato.format(oConn.setResult.getDate("fecha_perimetro"));
                     model.addRow(registros);
                }
                 
                  // Coloca el Modelo de Nueva Cuenta
                  tabla.setModel(model);
                tabla(tabla);
             
                 // Cierra Resultados
                 oConn.setResult.close();
            } 
            catch (SQLException ex) 
            {
                //JOptionPane.showMessageDialorootPane,ex);
                oFunc.SubSistemaMensajeError(ex.toString());
                Logger.getLogger(Audiometria.class.getName()).log(Level.SEVERE, null, ex);
            }
        }    
         
}
