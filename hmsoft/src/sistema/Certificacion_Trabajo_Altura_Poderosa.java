    private void txtNordenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtNordenActionPerformed
         
        if(!txtNorden.getText().isEmpty()){
          if(!oPe.nOrden(txtNorden, "certificado_altura_poderosa"))  {
        FechaNacimiento = new com.toedter.calendar.JDateChooser();
     String Sql="select d.nombres_pa||' '||d.apellidos_pa AS nombre, d.sexo_pa, d.cod_pa, d.fecha_nacimiento_pa , "
             + "n.cargo_de ,n.razon_empresa,n.nom_examen from datos_paciente AS d "
             + "INNER JOIN n_orden_ocupacional AS n ON (d.cod_pa=n.cod_pa) WHERE n.n_orden ='"+txtNorden.getText().toString()+"'";
               oConn.FnBoolQueryExecute(Sql);      
             try {
                    if (oConn.setResult.next()) {                        
                        txtNombres.setText(oConn.setResult.getString("nombre"));
                        txtdni.setText(oConn.setResult.getString("cod_pa"));
                        FechaNacimiento.setDate(oConn.setResult.getDate("fecha_nacimiento_pa"));
                        txtsexo.setText(oConn.setResult.getString("sexo_pa").equals("M")? "MASCULINO":"FEMENINO") ;
                        txtempresa.setText(oConn.setResult.getString("razon_empresa"));
                        txtareatrabajo.setText(oConn.setResult.getString("cargo_de"));
                        txtNorden.setEditable(false);
                        FechaExamen.requestFocusInWindow();
                        txtEdad.setText(String.valueOf(oFunc.calcularEdad(FechaNacimiento.getCalendar())) );
                        oPe.fecha(FechaExamen);
                        String exa=oConn.setResult.getString("nom_examen");
//                        txtExamen.setText(exa);
                       
                        fechaHasta();
                        chk();
                        muestraVisual();
                        triaje();
                        rayosx();
//                        Audiometria();
                        
                        txtexperiencia.requestFocus();
                       }else{
                        oFunc.SubSistemaMensajeError("No se encuentra Registro: \n 1- Intente de nuevo \n 2- Si el error sigue Registre Usuario o \n    Aperture EX-Preocupacional");
                    }
            } catch (SQLException ex) {
            oFunc.SubSistemaMensajeInformacion("Error:" + ex.getMessage().toString());
            }
          }else{
              oFunc.SubSistemaMensajeError("Número de Orden Utilizado");
          }
        }
    }//GEN-LAST:event_txtNordenActionPerformed
