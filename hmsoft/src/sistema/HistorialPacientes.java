private void CargarDatos(String valor){
   
    // SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
   String [] titulos={"DNI","Nombres y Apellidos","Nro Historia"};
    String [] registros = new String[3];    
   model = new DefaultTableModel(null,titulos){
            
              @Override
          public boolean isCellEditable(int rowIndex, int columnIndex) {
        return false;
    }
          
          };
    String Sql="SELECT cod_pa,nombres_pa||' '||apellidos_pa AS nombres, historial_pa FROM datos_paciente "
            + "WHERE CONCAT(nombres_pa,' ',apellidos_pa,' ',cod_pa) LIKE '%"+valor+"%'";
    
               
                if (oConn.FnBoolQueryExecute(Sql))
        {
             try  {
                
                while (oConn.setResult.next())
                {
                                     
                    registros[0]= oConn.setResult.getString("cod_pa");
                    registros[1]= oConn.setResult.getString("nombres");
                   // registros[2]= formato.format(oConn.setResult.getDate("historial_pa"));
                    registros[2]= oConn.setResult.getString("historial_pa");
                    model.addRow(registros);
                }
                
                  // Coloca el Modelo de Nueva Cuenta
                  tbHP.setModel(model);
               sbImp();
             
                 // Cierra Resultados
                 oConn.setResult.close();
            } 
            catch (SQLException ex) 
            {
                //JOptionPane.showMessageDialorootPane,ex);
                oFunc.SubSistemaMensajeError(ex.toString());
                Logger.getLogger(Odontograma.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
            try {
                oConn.sqlStmt.close();
            } catch (SQLException ex) {
                Logger.getLogger(HistorialPacientes.class.getName()).log(Level.SEVERE, null, ex);
            }
           
}

